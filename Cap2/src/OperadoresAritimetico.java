
public class OperadoresAritimetico {
	public static void main(String[] args) {
		
		Integer notaAluno1 = 100;
		Integer notaAluno2 = 76;
		Integer notaAluno3 = 90;
		
		Integer totalGeral = notaAluno1 + notaAluno2 + notaAluno3;
		
		Integer mediaGeral = (notaAluno1 + notaAluno2 + notaAluno3)/3; 
		
		System.out.println(totalGeral);
		System.out.println(mediaGeral);
	}

}
